#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int lon;

	lon = 0;
	while (*str)
	{
		if (*str >= 97 && *str <= 122)
		{
			*str = *str - 32;
		}
		str++;
		lon++;
	}
	str = str - lon;
	return (str);
}
